
package constructors;


public class MyClass {

    
    //Default Const.
    public MyClass() {
        System.out.println("This is MyClass DEFAULT CONSTRUCTOR");
    }
    
    // Parametric Cons.
    
    public MyClass(int x) {
        System.out.println("PARAMETRIC CONSTRUCTOR of MyClass");
        System.out.println("" + x);
    }
    
}
